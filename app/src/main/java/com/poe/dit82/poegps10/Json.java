package com.poe.dit82.poegps10;

public class Json {

    private String opr;
    private double lat;
    private double lng;
    private String provod;

    Json () {}

    public void setOpr(String opr) {
        this.opr = opr;
    }
    public String getOpr() {
        return opr;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }
    public double getLat() {
        return lat;
    }

    public void setLng(double lng) {
        this.lng = lng;
    }
    public double getLng() {
        return lng;
    }

    public void setProvod(String provod) {
        this.provod = provod;
    }
    public String getProvod() {
        return provod;
    }



}
